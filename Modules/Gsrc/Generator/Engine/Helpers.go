package Engine

import (
	"encoding/json"
	"io/ioutil"
	FrizzHelp "main/Modules/Gsrc/Helpers"
	"os"
)

// Function allows for constant ranges, instead of constant for loops
func Marshal(structure interface{}, filename string) {
	f, _ := json.MarshalIndent(structure, "", "")
	_ = ioutil.WriteFile(filename, f, 0644)
}

func ReturnJsonInterface(filename string) []byte {
	data, x := FrizzHelp.Fjloader(filename)
	if x != nil {
		os.Exit(1)
		return nil
	} else {
		return data
	}
}
